package loopingstatements;

public class switchcase {
	public static void main(String[] args) {
		int day=1;
		switch(day) {
		case 1:
			System.out.println("sunday");
			break;
		case 2:
			System.out.println("monday");
			break;
		case 3:
			System.out.println("tuesday");
		default:
			System.out.println("not availble");
		}
	}

}
